class Транспорт:
    def __init__(self, марка):
        self.марка = марка
        print(f"Конструктор Транспорт: {self.марка}")
    
    def __del__(self):
        print(f"Деструктор Транспорт: {self.марка}")


class Автомобиль(Транспорт):
    def __init__(self, марка, двери):
        super().__init__(марка)
        self.двери = двери
        print(f"Конструктор Автомобиль: {self.двери} дверей")
    
    def __del__(self):
        print(f"Деструктор Автомобиль: {self.двери} дверей")
        super().__del__()


class Электромобиль(Автомобиль):
    def __init__(self, марка, двери, батарея):
        super().__init__(марка, двери)
        self.батарея = батарея
        print(f"Конструктор Электромобиль: батарея {self.батарея} кВт")
    
    def __del__(self):
        print(f"Деструктор Электромобиль: батарея {self.батарея} кВт")
        super().__del__()


print("Создаем Транспорт:")
т = Транспорт("Велосипед")

print("\nСоздаем Автомобиль:")
а = Автомобиль("Toyota", 4)

print("\nСоздаем Электромобиль:")
э = Электромобиль("Tesla", 4, 75)


print("Удаляем Транспорт:")
del т

print("\nУдаляем Автомобиль:")
del а

print("\nУдаляем Электромобиль:")
del э
